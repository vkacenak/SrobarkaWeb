      <li><b>1. Zmeny vyhlášky č. 318/2008 Z. z.</b> o ukončovaní štúdia na stredných školách v znení neskorších
predpisov:</li>
  <p> <b>a) účinné od 1. septembra 2018:</b>
Zmena spôsobu testovania <b>EČ MS zo slovenského jazyka a slovenskej literatúry</b> (školy s
vyučovacím jazykom maďarským): EČ MS zo slovenského jazyka a slovenskej literatúry bude
overovať <b>komunikačné kompetencie v častiach počúvanie s porozumením</b>, čítanie s porozumením
a jazyk a literatúra. V rámci počúvania s porozumením sa bude preverovať, ako vedia žiaci efektívne
počúvať hovorený prejav v slovenskom jazyku, porozumieť mu a získať z neho informácie, ktoré sú
potrebné na splnenie úloh v teste podobne ako v cudzom jazyku.</p>
  <p> <b>b) účinné od 1. septembra 2016:</b>
Nastavenie času administrácie testov EČ MS: z druhých vyučovacích jazykov úroveň C1 – anglický,
francúzsky, nemecký, ruský, španielsky, taliansky jazyk (žiaci bilingválnych stredných škôl) <b>zo 120 na
150 minút.</b> Nastavenie času administrácie PFIČ MS: z druhých vyučovacích jazykov úroveň C1 –
anglický, francúzsky, nemecký, ruský, španielsky, taliansky jazyk (žiaci bilingválnych stredných škôl)
<b>zo 60 na 90 minút.</b></p>
  <p> <b>c) účinné od 1. septembra 2015:</b>
Predĺženie času administrácie testov EČ MS: i. zo slovenského jazyka a literatúry, maďarského jazyka
a literatúry, ukrajinského jazyka a literatúry a slovenského jazyka a slovenskej literatúry z 90 na 100
minút, ii. z matematiky <b>zo 120 na 150 minút</b>.</p>
  <p> <b>d) účinné od 1. septembra 2013:</b>
Vzorec na výpočet stupňa prospechu zo slovenského jazyka a literatúry:
V predmete slovenský jazyk a literatúra je súčasťou maturitných zadaní aj <b>vzorec na výpočet stupňa
prospechu</b> (vyhláška o ukončovaní štúdia na stredných školách v znení neskorších predpisov, § 8,
odsek 2). Každé maturitné zadanie obsahuje dve úlohy a súvisiace východiskové texty. Prvá úloha
obsahuje učivo zo slovenského jazyka. Druhá úloha obsahuje učivo z literatúry (vyhláška o
ukončovaní štúdia na stredných školách v znení neskorších predpisov, Príloha III. časť, predmet
slovenský jazyk a literatúra, úvodný odsek). Váha hodnotenia jednotlivých úloh je 3 : 2 alebo 1 : 1, pre
gymnáziá s osemročným vzdelávacím programom môže byť v pomere 2 : 3, ale len do 31. augusta
2015 (vyhláška o ukončovaní štúdia na stredných školách v znení neskorších predpisov, Príloha III.
časť, predmet slovenský jazyk a literatúra, Hodnotenie, písmená c, d).</p>


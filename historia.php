<div class="col-md-4 historia-starting"><p class="historia-border">História našej „Šrobárky“ siaha do rokov 1892 – 1896. V týchto rokoch vyrástla na terajšej
Šrobárovej ulici nová elegantná štýlová budova - budova Štátnej vyššej dievčenskej školy. Výstavbu
viedli známi košickí stavitelia bratia Jakabovci. Škola predstavovala nový typ vzdelávacej inštitúcie
určenej výhradne pre dievčatá. V roku jej založenia na nej študovalo 236 študentiek. Okrem
bežných predmetov sa klasifikovalo aj správanie, usilovnosť, kreslenie, krasopis a ručné práce. V roku
1894 bola riadením školy poverená Elza Kolacskovszká, ktorá je dodnes najdlhšie pôsobiacou
riaditeľkou v histórii školy. Druhé poschodie budovy bolo vyčlenené pre účely internátu, ktorý
poskytol ubytovanie pre 40 dievčat. Po rozpade Rakúsko - Uhorska z Vyššej dievčenskej
školy vzniklo Dievčenské gymnázium. Okrem názvu školy sa zmenil aj vyučovací jazyk.
Prvýkrát sa začína vyučovanie v slovenčine (alebo českoslovenčine ako sa úradne nazýval
nový štátny jazyk). Rok 1923 priniesol ďalší zásadný moment v živote študentiek, 28. júna sa
uskutočnili prvé maturitné skúšky. Nová škola však musela čeliť veľkej kritike zo strany
maďarského obyvateľstva.</p></div>
<div class="col-md-4"><p class="historia-border">Napriek tomu si učitelia gymnázia postupne získali dôveru rodičov
a už koncom 30. rokov malo gymnázium nezastupiteľné miesto medzi košickými školami.
Žiačkami gymnázia boli najmä dievčatá zo zámožnejších rodín, keďže školné a zápisné bolo
na dané časy veľmi vysoké. Škola vyriešila aj tento problém, a to zavedením štipendií, ktoré
sa udeľovali podľa výšky príjmu rodičov. Pokojné a úspešné fungovanie gymnázia narušilo
pričlenenie Košíc k Maďarsku a následné vypuknutie druhej svetovej vojny. Po ukončení vojny
bola činnosť gymnázia obnovená a zároveň sa v novej Československej republike prijali zákony, ktoré
povolili spoločné vzdelávanie dievčat a chlapcov. V školskom roku 1948/49 gymnázium otvorene
vítalo aj chlapcov, ktorí sa tiež pričinili o jej zviditeľnenie a reprezentáciu v rôznych
vedomostných, literárnych a športových súťažiach. Koncom roku 1967 sa prvýkrát konali
stužkové slávnosti absolventov v Dome umenia. 19. decembra tohto roku sa položil základ</p></div>
<div class="col-md-4"> <img src="img/historia.jpg"><p >tradície, ktorá pretrváva dodnes. Koncom 70. rokov sa vzdelávalo na našej škole už okolo 1000
študentov. Sedemdesiate roky boli pre školu naozaj veľmi úspešne. Život študentov
obohacovali lyžiarske kurzy, divadelné predstavenia a výlety do blízkych ale aj vzdialenejších
miest. Najobľúbenejšia bola návšteva Prahy, Brna či Krakova. Samozrejme nesmeli chýbať
ani každoročné zemiakové brigády a každých päť rokov vyčerpávajúca príprava na
spartakiádu. Tradícia, ktorá najviac formuje “ducha“ Šrobárky sú Kalamajky. Prvýkrát sa
uskutočnili v roku 1974 a od tohto roku žiaci nášho gymnázia každoročne koncom apríla
zabávajú publikom svojimi divadelnými scénkami, tancom a spevom. Deväťdesiate roky
priniesli zmeny v osnovách a v orientácii školy, sú to roky vzniku nových tradícií. Vzniklo
Gymnázium Šrobárova 1 a vstúpilo do podoby, v akej ho poznáte dnes.</p></div>
